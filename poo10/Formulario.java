    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        int numeroInscripcion;
        String nombres;
        double patrimonio;
        double estratoSocial;
        double valorMatricula;
        double incremento;
        
        patrimonio = Double.parseDouble(txtPatrimonio.getText());
        estratoSocial = Double.parseDouble(txtEstratoSocial.getText());
        
        double VALOR_CONSTANTE = 50000;  // valor constante para cada estudiante
        double PORCENTAJE = 0.03; //porcentaje de aumento de la matricula
        if ((patrimonio > 2000000)&&(estratoSocial>3)){
            
            incremento = patrimonio * PORCENTAJE;
            valorMatricula = VALOR_CONSTANTE + incremento;

        }
        else {
            
            valorMatricula = VALOR_CONSTANTE;
            
        }
        
        txtValorMatricula.setText(String.valueOf(valorMatricula));
    }//GEN-LAST:event_btnCalcularActionPerformed

