    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        var desicion = Double.parseDouble(txtDesicion.getText());
        
        if (desicion == 1){
            
            FormCirculo form = new FormCirculo();
            form.setVisible(true); 
        }
        else if (desicion == 2){
            
            FormCuadrado form = new FormCuadrado();
            form.setVisible(true);
            
        }
        else if (desicion == 3){
            
            FormCuadrado form = new FormCuadrado();
            form.setVisible(true);
            
        }
        else if (desicion == 4){
            
            FormTriangulo form = new FormTriangulo();
            form.setVisible(true);
            
        }
        else if (desicion == 5){
            
            FormRombo form = new FormRombo ();
            form.setVisible(true);
            
        }
        else if (desicion == 6){
            
            FormTrapecio form = new FormTrapecio ();
            form.setVisible(true);
        }
        else{
            txtDesicion.setText(String.valueOf("Ingrese una opci√≥n valida"));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

