    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double salarioBruto, salarioNeto, montoRetencion;
        int codigo; 
        String nombre;
        double horasTrabajadasMes;
        double valorHora;
        double porcRetencion;
        
        horasTrabajadasMes = Double.parseDouble(txtHorasTrabajadas.getText());
        valorHora = Double.parseDouble(txtValorHora.getText());
        porcRetencion = Double.parseDouble(txtPorcentajeRetencion.getText());
        
        salarioBruto = horasTrabajadasMes * valorHora;
        montoRetencion = salarioBruto * porcRetencion;
        salarioNeto = salarioBruto - montoRetencion;
        
        txtSalarioBruto.setText(String.valueOf(salarioBruto));
        txtSalarioNeto.setText(String.valueOf(salarioNeto));
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

